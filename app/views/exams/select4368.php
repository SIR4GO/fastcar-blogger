	القيادة الوقائية و الامنه تحت ظروف مختلفة<br />
<br />
1.	إذا انفجر الإطار الامامي فجاه لا يجب استخدام الفرامل في إيقاف  السيارة <br />
2.	للقيادة في المطر يجب الالتزام بسرعة اقل من السرعة القانونية <br />
3.	استخدام الفرامل اثناء المطر يؤدي إلي إيقاف السيارة في مسافة اكبر مما لو كان الطريق جاف <br />
4.	في حالة القيادة في الشبورة المائية يجب مضاعفة مسافة الامان بين سيارتك و السيارة التي امامك <br />
5.	في حالة القيادة في الضباب يجب تهدئة السرعة <br />
6.	أقص مده يمكن إن تقود بها سيارتك ساعتين متصلين ثم الإستراحة قبل معاوده القيادة <br />
7.	للقيادة في المطر يجب إن تكون مساحات المطر صالحة و بحالة جيدة <br />
8.	القيادة في المطر يجب مضاعفة مسافة الامان الامامية و الجانبية <br />
9.	القيادة في الشبورة المائية يجب إستخدام انوار الانتظار<br />
10.	بمجرد تخطئ السيارة التي امامك يجب العودة تدريجيا إلي خط السير الطبيعي <br />
11.	اثناء قيادتك فوجئت بسيارة مقطورة تحاول قطع الطريق ماذا تفعل :-  اهدي السرعة مع استخدام اله التنبيه و اعطي له الفرصة للمرور<br />
12.	في حالة الرجوع بالمركبة للخلف إلا يجاوز الرجوع إلي الخلف مسافة تعادل طول المركبة <br />
13.	يجب اثناء التخطي ترك مسافة جانبية كافية عن السيارة التي تتخطاها <br />
14.	وجدت الطريق به حفر تعوق السير و علي يمينك طريق جانبي فماذا تفعل :- تسير ببطئ حتي تمر <br />
15.	و انت تسير فوق كوبري علوي انفجرت احدي الإطارات فما هو تصرفك :-  انزل من فوق الكوبري و احاول تغييره في مكان اخر <br />
16.	للقيادة في المطر يجب عدم استخدام فرامل اليد بعنف <br />
17.	اثناء القيادة حدث لك شد عضلي . فماذا تفعل :-   توقف السيارة علي يمين الطريق لحين الإنتهاء من الشد العضلي <br />
18.	يجب علي قائد السيارة عند إيقاف سيارته اثناء السير إن يترك مسافة بينه و بين السيارة التي امامه <br />
19.	يجب عند نزول المنحدرات إن تقوم بتعشيق عصا نقل السرعات ( الفتيس ) <br />
20.	لا يجوز التوقف او الانتظار امام محطات البنزين و مراكز الإطفاء او الشرطة <br />
21.	لا يمكن القيادة ليلا باستخدام انوار الانتظار فقط <br />
22.	لا يجوز لقائد المركبة قيادتها دون إن تكون مزوده بمراه عاكسة تمكنه من كشف المركبات خلفه <br />
23.	لا يمكن الرجوع بالسيارة للخلف لمسافة خمسون مترا ( 50 متر ) <br />

